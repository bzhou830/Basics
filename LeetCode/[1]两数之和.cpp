class Solution {
public:
    vector<int> twoSum(vector<int>& nums, int target) {
        vector<int> res;
		if(nums.size() <= 1)
			return res;
		unordered_map<int, int> h;
		for(size_t i=0;i<nums.size();++i) {
			if(h[target- nums[i]] > 0){
				res.push_back(h[target- nums[i]] - 1);
				res.push_back(i);
				return res;
			}
			h[nums[i]] = i + 1;
		}
		return res;
    }
};
