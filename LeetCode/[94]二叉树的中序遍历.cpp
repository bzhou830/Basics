/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
	vector<int> res;
    vector<int> inorderTraversal(TreeNode* root) {
        res.clear();
		inorder(root);
		return res;
    }
	
	void inorder(TreeNode* root){
		if(root->left)
			inorder(root->left);
		res.push_back(root->val);
		if(root->right)
			inorder(root->right);
	}
	
};
